digits = [["-**--","*--*-","*--*-","*--*-","-**--","-----"],
["--*--","-**--","--*--","--*--","-***-","-----"],
["***--","---*-","-**--","*----","****-","-----"],
["***--","---*-","-**--","---*-","***--","-----"],
["-*---","*--*-","****-","---*-","---*-","-----"],
["****-","*----","***--","---*-","***--","-----"],
["-**--","*----","***--","*--*-","-**--","-----"],
["****-","---*-","--*--","-*---","-*---","-----"],
["-**--","*--*-","-**--","*--*-","-**--","-----"],
["-**--","*--*-","-***-","---*-","-**--","-----"]]

file = ARGV[0]

File.open(file).each do |line|

	split_num = line.to_s.gsub(/[^0-9a-z]/i, '').split('')
	all_numbers = []
	
	split_num.each { |x| all_numbers << digits[x.to_i] }	
	
	all_numbers.transpose.each { |a| puts "#{a.join}" }
	
end